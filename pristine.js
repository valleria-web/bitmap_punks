function createPixelArt() {
  // Clear any existing content in the container
  pixelArtContainer.innerHTML = '';

  // Loop through the rows and columns of the grid
  for (let row = 0; row < gridSize; row++) {
    for (let col = 0; col < gridSize; col++) {
      // Create a new div element for each pixel
      const pixel = document.createElement('div');
      pixel.classList.add('pixel');

      // Check if the current position should be colored orange
      if (pixelArtPattern[row] && pixelArtPattern[row][col] === 1) {
        pixel.style.backgroundColor = orange; // Set the color to orange
      }

      // Append the pixel to the container
      pixelArtContainer.appendChild(pixel);
    }
  }
}

// Call the function to generate the pixel art
